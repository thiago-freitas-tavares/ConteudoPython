# List (podem ser modificadas - entre colchetes)

nomes = ["Naty", "Natily", "Lorraina", "Albuquerque", "Orleans", "Bragança"]
print(nomes)
nomes[0] = "Nati" # modifica item da lista

print(nomes[0])
print(nomes[5])
print(nomes[-1])
print(nomes[-3])

print(nomes[0:3])       # não altera a lista original
print(nomes[2:])
